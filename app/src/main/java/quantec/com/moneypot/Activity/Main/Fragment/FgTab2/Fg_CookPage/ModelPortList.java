public class ModelPortList {

    String stCode;
    String stname;

    public ModelPortList(String stCode, String stname) {
        this.stCode = stCode;
        this.stname = stname;
    }

    public String getStCode() {
        return stCode;
    }

    public void setStCode(String stCode) {
        this.stCode = stCode;
    }

    public String getStname() {
        return stname;
    }

    public void setStname(String stname) {
        this.stname = stname;
    }
}
